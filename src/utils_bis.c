#include	"../include/cub3d.h"

int	check_fc(char **ss, int i)
{
	int	x;

	x = 0;
	if (ss[i])
	{
		while (ss[i][x])
		{			
			if (!ft_isdigit(ss[i][x]) && ss[i][x] != ',')
				return (0);
			x++;
		}
	}
	return (1);
}
